# -------------------------------------------------------------------------
# ---------------------------- Libraries/Packages -------------------------
# -------------------------------------------------------------------------

box::use(
  leaflet[leafletOutput, renderLeaflet],
  shiny[moduleServer, NS, tagList]
)

# -------------------------------------------------------------------------
# ---------------------------------- Modules ------------------------------
# -------------------------------------------------------------------------

box::use(
  app/logic[mbta_map_logic]
)

# -------------------------------------------------------------------------
# ------------------------------ UI Function ------------------------------
# -------------------------------------------------------------------------
#' @export
init_ui <- function(id) {
  ns <- NS(id)
  tagList(
    leafletOutput(outputId = ns("mbta_leaflet_map"), height = "100%")
  )
}

# -------------------------------------------------------------------------
# ----------------------------- Server Function ---------------------------
# -------------------------------------------------------------------------
#' @export
init_server <- function(id, map_data) {
  moduleServer(
    id,
    function(input, output, session) {
      # ----------------------------------
      # ----- Reactive Map Creation ------
      # ----------------------------------
      output$mbta_leaflet_map <- renderLeaflet({
        mbta_map_logic$build_mbta_map(map_data = map_data(), year = "2019")
      })
    }
  )
}
